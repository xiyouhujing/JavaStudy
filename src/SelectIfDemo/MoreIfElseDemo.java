package SelectIfDemo;

public class MoreIfElseDemo {
    public static void main(String[] args) {
        int x = 3;
        if (x ==1) {
            System.out.println("x的值是1！");
        }else if (x == 2) {
            System.out.println("x的值是2！");
        }else if (x == 3) {
            System.out.println("x的值是3！");
        }else {
            System.out.println("x的值不是1、2、3中的一个！");
        }
    }
}
