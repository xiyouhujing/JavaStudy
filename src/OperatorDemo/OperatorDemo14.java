package OperatorDemo;

public class OperatorDemo14 {
    public static void main(String[] args) {
        int result1 = 3 + 5 + 4 * 6 - 7;
        int result2 = ((3 + 5 + 4) * (6 - 7));
        System.out.println("3 + 5 + 4 * 6 - 7 = " + result1);
        System.out.println("3 + 5 + 4 * 6 - 7 = " + result2);
    }
}
