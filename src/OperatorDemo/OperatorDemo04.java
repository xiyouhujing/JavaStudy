package OperatorDemo;

public class OperatorDemo04 {
    public static void main(String[] args) {
        System.out.println("3 > 1 = " + (3 > 1));
        System.out.println("3 < 1 = " + (3 < 1));
        System.out.println("3 >= 1 = " + (3 >= 1));
        System.out.println("3 <= 1 = " + (3 <= 1));
        System.out.println("3 == 1 = " + (3 == 1));
        System.out.println("3 != 1 = " + (3 != 1));
    }
}
